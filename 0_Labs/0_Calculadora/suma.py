def suma(numero_uno, numero_dos):
    numero_uno = int(numero_uno)
    numero_dos = int(numero_dos)
    return numero_uno + numero_dos

def suma_uno(numero_uno, numero_dos):
    numero_uno = int(numero_uno)
    numero_dos = int(numero_dos)
    return numero_uno + numero_dos + 10

if __name__ == "__suma__":
    suma()
    suma_uno()